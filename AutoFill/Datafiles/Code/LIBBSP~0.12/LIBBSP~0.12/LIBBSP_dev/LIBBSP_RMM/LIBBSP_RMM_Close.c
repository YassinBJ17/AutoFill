TE_LIBBSP_I2C_Code LIBBSP_RMM_Close(void)
{
   TE_LIBBSP_I2C_Code v_rc;

   v_rc = E_LIBBSP_I2C_CLOSED;

   if (V_RMM_State == E_LIBBSP_RMM_OPEN)
   {
      V_RMM_State = E_LIBBSP_RMM_CLOSED;
      v_rc = LIBBSP_I2C_Close(E_LIBBSP_I2C_BUS_2);
   }

   return (v_rc);
}
