TE_LIBBSP_I2C_Code LIBBSP_I2C_Initialize(void)
{
    uint32_t v_i;

    for (v_i = 0ul; v_i < E_LIBBSP_I2C_NB_I2C_BUS; ++v_i)
    {
        V_I2C_State[v_i] = E_LIBBSP_I2C_BUS_CLOSED;
    }

    return (E_LIBBSP_I2C_OK);
}
